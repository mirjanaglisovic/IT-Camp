// 6. Tipovi operatora.

// 1.1. Sabiranje:
console.log(5 + 5);

// 1.2. Oduzimanje:
console.log(7 - 3);

// 1.3. Mnozenje:
console.log(3 * 5);

// 1.4. Deljenje:
console.log(15 / 3);

// 1.5. Stepenovanje:
console.log(2 ** 3);

// 1.6. Ostatak pri deljenju (%):
console.log(12 % 5);

// Da bi br. bio paran mora ostatak pri deljenju sa 2 biti nula.

// broj % 2 mora biti 0

// Da bi br. bio neparan mora ostatak pri deljenju sa 2 biti razlicit od nule.

// broj % 2 mora biti razlicit od 0.

// 1.7. Increment(++):
let a = 13;
a++;
console.log(a);

// 1.7. Decrement(--):
let b = 13;
b--;
console.log(b);
